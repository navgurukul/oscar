function HomeTab({
  userName,
  isRecording,
  onToggleRecording,
  recordingTime,
  totalNotes,
}: HomeTabProps) {
  // Extract first name
  const firstName = userName?.split(" ")[0] || "";
  const greeting = firstName ? `Welcome back, ${firstName}` : "Welcome back";

  return (
    <div className="home-container">
      <div className="home-content">
        {/* Greeting */}
        <h1 className="home-greeting">{greeting}</h1>

        {/* Record Button */}
        <div className="home-record-wrapper">
          {isRecording && <div className="home-record-ripple" />}
          <button
            className={`home-record-btn ${isRecording ? "recording" : ""}`}
            onClick={onToggleRecording}
            aria-label={isRecording ? "Stop recording" : "Start recording"}
          >
            {isRecording ? (
              <Square size={40} fill="white" />
            ) : (
              <Mic size={48} />
            )}
          </button>
        </div>

        {/* Recording Timer */}
        <div className={`home-timer ${isRecording ? "active" : ""}`}>
          {formatTime(recordingTime)}
        </div>

        {/* Instruction Text */}
        <p className="home-instruction">
          {isRecording
            ? "Recording... Tap to stop"
            : "Tap the microphone to start recording"}
        </p>

        {/* Minimal Stats */}
        {typeof totalNotes === "number" && (
          <p className="home-stats">
            {totalNotes} {totalNotes === 1 ? "note" : "notes"} recorded
          </p>
        )}

        {/* Shortcut Section */}
        <div className="home-shortcut-card">
          <p className="home-shortcut-text">
            Hold <kbd className="home-kbd">Ctrl</kbd> + <kbd className="home-kbd">Space</kbd> to record from anywhere
          </p>
          <div className="home-app-icons">
            <AppIcons.Slack />
            <AppIcons.Notion />
            <AppIcons.VSCode />
            <AppIcons.GoogleDocs />
            <AppIcons.Chrome />
            <AppIcons.Discord />
            <AppIcons.Teams />
            <AppIcons.Figma />
          </div>
          <p className="home-works-everywhere">Works everywhere</p>
        </div>
      </div>
    </div>
  );
}

export default HomeTab;
